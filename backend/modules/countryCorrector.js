function getLevenshteinDistance(str1, str2) {
  const track = Array(str2.length + 1).fill(null).map(() => Array(str1.length + 1).fill(null));
  for (let i = 0; i <= str1.length; i += 1) {
    track[0][i] = i;
  }
  for (let j = 0; j <= str2.length; j += 1) {
    track[j][0] = j;
  }
  for (let j = 1; j <= str2.length; j += 1) {
    for (let i = 1; i <= str1.length; i += 1) {
      const indicator = str1[i - 1] === str2[j - 1] ? 0 : 1;
      track[j][i] = Math.min(
        track[j][i - 1] + 1,
        track[j - 1][i] + 1,
        track[j - 1][i - 1] + indicator
      );
    }
  }
  return track[str2.length][str1.length];
}

function correctCountryName(input) {
  if (!input) return '';
  const cleanInput = input.trim().toLowerCase();

  // Try exact match
  const exactMatch = countriesList.find(c => c.toLowerCase() === cleanInput);
  if (exactMatch) return exactMatch;

  let bestMatch = null;
  let minDistance = Infinity;

  for (const country of countriesList) {
    const dist = getLevenshteinDistance(cleanInput, country.toLowerCase());
    if (dist < minDistance) {
      minDistance = dist;
      bestMatch = country;
    }
  }

  // Set threshold
  const maxThreshold = Math.max(2, Math.floor(cleanInput.length * 0.4));
  if (minDistance <= maxThreshold) {
    return bestMatch;
  }

  // If input is completely wrong, return null to indicate failure
  return null;
}

